package com.jojowonet.modules.order.form.vo;

import ivan.common.utils.excel.annotation.ExcelField;

public class StaffStatsVo {
    private String area;
    private String areaManagerName;
    private String siteName;
    private Long cnt; // 服务工程师个数
    private Long mcount; // 信息员个数

    @ExcelField(title = "区域", align = 2, sort=0)
    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    @ExcelField(title = "区管姓名", align = 2, sort=1)
    public String getAreaManagerName() {
        return areaManagerName;
    }

    public void setAreaManagerName(String areaManagerName) {
        this.areaManagerName = areaManagerName;
    }

    @ExcelField(title = "服务商名称", align = 2, sort=2)
    public String getSiteName() {
        return siteName;
    }

    public void setSiteName(String siteName) {
        this.siteName = siteName;
    }

    @ExcelField(title = "服务工程师人数", align = 2, sort=3)
    public Long getCnt() {
        return cnt;
    }

    public void setCnt(Long cnt) {
        this.cnt = cnt;
    }

    @ExcelField(title = "信息员人数", align = 2, sort=4)
    public Long getMcount() {
        return mcount;
    }

    public void setMcount(Long mcount) {
        this.mcount = mcount;
    }
}
