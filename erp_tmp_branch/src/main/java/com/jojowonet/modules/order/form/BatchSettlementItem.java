package com.jojowonet.modules.order.form;

public class BatchSettlementItem {
    private String id;
    private String label;
    private String value;
    private double calculatedValue; // 有些value是比例，这个值是根据比例计算出来的值，初始值为０，在处理此BatchSettlementItem时，计算出来。
    private boolean p;
    private String type; // 对应结算模板的basis_type

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public boolean isP() {
        return p;
    }

    public void setP(boolean p) {
        this.p = p;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public double getCalculatedValue() {
        return calculatedValue;
    }

    public void setCalculatedValue(double calculatedValue) {
        this.calculatedValue = calculatedValue;
    }
}
